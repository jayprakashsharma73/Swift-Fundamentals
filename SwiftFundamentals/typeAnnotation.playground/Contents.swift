//Type Annotation - Defining variable/constatnt data types
var name:String
var age:Int =  25
print(type(of: name))
print(type(of: age))

//Type Inference - Data types Automatically inferred by compiler based on values/literals
let lastName = "Sharma"
let contactNumber = 8946063740
// Decimal number inferred as Double by default
var percentage =  78.32
print(type(of: percentage))
